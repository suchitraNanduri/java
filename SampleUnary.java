class SampleUnary
{
  public static void main(String args[])
{
   int a=10;
   System.out.println("The value of a is "+a);
   int b=++a;
   System.out.println("The value of b is "+b);
   int c = a++;
   System.out.println("The value of c is "+c);
   System.out.println("The Final value of a is"+a);
  
}
}